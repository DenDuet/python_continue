class Square:
    
    def __init__(self,long,width=0):
        self.long = long
        if width == 0:
            self.width = long
        else:
            self.width = width
    
        
    def sqr(self):
        return self.long * self.width
    
    def perim(self):
        return 2 * (self.long + self.width)
    
p = Square(10)
print(f"{p.sqr()=}, {p.perim()=}")

p = Square(10,20)
print(f"{p.sqr()=}, {p.perim()=}")