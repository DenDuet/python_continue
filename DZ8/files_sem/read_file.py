# üìå –ö–∞–∂–¥—É—é –ø–∞—Ä—É —Å–æ—Ö—Ä–∞–Ω—è–π—Ç–µ —Å –Ω–æ–≤–æ–π —Å—Ç—Ä–æ–∫–∏.
__all__ = ['read_file']

import json


def read_file(fname: str='result.txt') -> list:
    """–§—É–Ω–∫—Ü–∏—è –ø–æ–ª—É—á–∞–µ—Ç –∏–º—è —Ç–µ–∫—Å—Ç–æ–≤–æ–≥–æ —Ñ–∞–π–ª–∞ —Å –¥–∞–Ω–Ω—ã–º–∏ –∏ —Å–æ–∑–¥–∞–µ—Ç –∏–∑ –Ω–∏—Ö —Å–ª–æ–≤–∞—Ä—å, –∫–æ—Ç–æ—Ä—ã–π –ø–µ—Ä–µ–¥–∞–µ—Ç –≤ —Ñ—É–Ω–∫—Ü–∏—é —Å–æ–∑–¥–∞–Ω–∏—è json —Ñ–∞–π–ª–∞"""

    dic = {}
    with (open(fname, 'r', encoding='UTF-8')) as fnm:
        lines = fnm.readlines()
        for line in lines:
            data = line.split(" ")
            dic.setdefault(data[0].capitalize(),data[2][:-1])
    return dic
            
            
def json_file(dic: dict, fname: str='result.json'):
    """–§—É–Ω–∫—Ü–∏—è –ø–æ–ª—É—á–∞–µ—Ç —Å–ø–∏—Å–æ–∫ —Å –¥–∞–Ω–Ω—ã–º–∏ –∏ —Å–æ–∑–¥–∞–µ—Ç –∏–∑ –Ω–∏—Ö json —Ñ–∞–π–ª"""
    with open(fname,'w',encoding="UTF-8") as fjs:
        json.dump(dic, fjs, indent=2, ensure_ascii=False)

                
if __name__ == '__main__':
    dic = read_file()
    # print(dic)
    json_file(dic)